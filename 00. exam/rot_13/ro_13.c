#include <unistd.h>

int main(int ac, char **av)
{
	int i;
	char j;
	
	i = 0;
	if (ac == 2)
	{
		while (av[1][i] != '\0')
		{
			j = av[1][i];
			
			if (av[1][i] >= 'a' && av[1][i] <= 'm')
				j += 13;
			else if (av[1][i] >= 'n' && av[1][i] <= 'z')
				j -= 13;
			else if (av[1][i] >= 'A' && av[1][i] <= 'M')
				j += 13;
			else if (av[1][i] >= 'N' && av[1][i] <= 'Z')
				j -= 13;
		
			write(1, &j, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return(0);

}
