#include <unistd.h>

void	wrt(char c)
{
	write (1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = -1;
	while (a++ < 98)
	{
		b = a;
		while (b++ < 99)
		{
			wrt (48 + (a / 10));
			wrt (48 + (a % 10));
			write (1, " ", 1);
			wrt (48 + (b / 10));
			wrt (48 + (b % 10));
			if (a != 98)
			{
				write (1, ", ", 2);
			}
		}
	}
}
