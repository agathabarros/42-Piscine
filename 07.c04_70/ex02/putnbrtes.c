#include <unistd.h>
#include <stdio.h>

void	ft_nbr(int nbr)
{
	if (nbr == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nbr < 0)
	{
		nbr = (nbr * -1);
		write (1, "-", 1);
	}
	if (nbr >= 10)
	{
		ft_nbr(nbr / 10);
		write(1, "nbr % 10 + '0'", 1);
	}
	
}
int main(void)
{
	ft_nbr(76890);
}
